import express from "express";
import {
  loginConPassword,
  loginConEmail,
  loginConWindows,
  logout
} from "../../Controladores/autenticacion/autenticacioncontroller.js";
import { verificarToken } from "../../middlewares/authMiddleware.js";

const router = express.Router();

//////////////////////////////////////////////////////////
// LOGIN NORMAL (usuario + contraseña)
//////////////////////////////////////////////////////////
router.post("/login", loginConPassword);

//////////////////////////////////////////////////////////
// LOGIN SOLO CON EMAIL (SOLICITANTES)
//////////////////////////////////////////////////////////
router.post("/login-email", loginConEmail);

//////////////////////////////////////////////////////////
// LOGIN CON WINDOWS / ACTIVE DIRECTORY
//////////////////////////////////////////////////////////
router.post("/login-windows", loginConWindows);

//////////////////////////////////////////////////////////
// LOGOUT
//////////////////////////////////////////////////////////
router.post("/logout", logout);

//////////////////////////////////////////////////////////
// VERIFY — ping para comprobar si el token sigue válido
//////////////////////////////////////////////////////////
router.get("/verify", verificarToken, (req, res) => {
  res.status(200).json({ ok: true });
});

export default router;